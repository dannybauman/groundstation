def md_to_html(md: str) -> str:
    out, in_list = [], False
    for line in md.splitlines():
        s = line.strip()
        if s.startswith("## "):
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append(f"<h2>{s[3:]}</h2>")
        elif s.startswith(("- ", "* ")):
            if not in_list:
                out.append("<ul>")
                in_list = True
            out.append(f"<li>{s[2:]}</li>")
        elif s:
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append(f"<p>{s}</p>")
    if in_list:
        out.append("</ul>")
    html = "\n".join(out)
    html = html.replace("**", "")  # keep it simple; bold markers read fine stripped
    return html


def synthesize(data: dict) -> str:
    prompt = SYNTH_PROMPT + json.dumps(data, default=str)
    try:
        r = subprocess.run(
            ["claude", "-p", "--model", "sonnet"],
            input=prompt,
            capture_output=True,
            text=True,
            timeout=240,
            shell=(sys.platform == "win32"),
        )
        if r.returncode == 0 and r.stdout.strip():
            return r.stdout.strip()
        print(f"claude -p failed ({r.returncode}): {r.stderr[:200]}", file=sys.stderr)
    except FileNotFoundError:
        print("claude CLI not found; writing data-only brief", file=sys.stderr)
    except subprocess.TimeoutExpired:
        print("claude -p timed out; writing data-only brief", file=sys.stderr)
    # Deterministic fallback so the pipeline never produces nothing
    ev = data["events"]
    lines = ["## TL;DR", f"Automated data summary for {data['place']} (no LLM synthesis available)."]
    lines += ["## What changed"] + [
        f"- {e['category']}: {e['title']} ({e.get('date','')})" for e in ev.get("eonet", [])[:8]
    ] or ["- No open events."]
    lines += ["## Fresh eyes on the ground"] + [
        f"- {i['collection']} {i['datetime'][:10]} cloud {i.get('cloud_cover')}%"
        for i in data["imagery"].get("items", [])[:5]
    ]
    return "\n".join(lines)


def ndvi_change(best: dict, today: dt.date, days: int) -> dict | None:
    """NDVI delta between the newest scene and a same-tile baseline ~1 month back."""
    tile = best["id"].split("_")[1] if "_" in best["id"] else None
    if not tile:
        return None
    end = today - dt.timedelta(days=days + 1)
    start = today - dt.timedelta(days=days + 45)
    baseline = tools.search_imagery(
        "earth-search",
        [best["collection"]],
        bbox=best["bbox"],
        datetime_range=f"{start.isoformat()}T00:00:00Z/{end.isoformat()}T23:59:59Z",
        max_cloud_cover=30,
        limit=20,
    )
    candidates = [i for i in baseline.get("items", []) if f"_{tile}_" in i["id"]]
    if not candidates:
        return None
    ref = min(candidates, key=lambda i: i.get("cloud_cover") or 100)
    expr = "(nir-red)/(nir+red)"
    now_s = tools.compute_statistics("earth-search", best["collection"], best["id"], expression=expr)
    ref_s = tools.compute_statistics("earth-search", ref["collection"], ref["id"], expression=expr)
    now_mean = next(iter(now_s.values()))["mean"]
    ref_mean = next(iter(ref_s.values()))["mean"]
    return {
        "metric": "NDVI (vegetation) over MGRS tile " + tile,
        "current": {"scene": best["id"], "date": best["datetime"][:10], "mean": round(now_mean, 3)},
        "baseline": {"scene": ref["id"], "date": ref["datetime"][:10], "mean": round(ref_mean, 3)},
        "delta": round(now_mean - ref_mean, 3),
        "delta_pct": round((now_mean - ref_mean) / abs(ref_mean) * 100, 1) if ref_mean else None,
    }


def build_brief(place: str, days: int, out_dir: Path) -> dict:
    today = dt.date.today()
    print(f"[1/5] geocoding {place!r}...")
    g = tools.geocode(place)
    if "error" in g:
        raise SystemExit(g["error"])
    bbox = g["bbox"]
    pad = 0.4  # look slightly beyond the AOI for nearby events
    ev_bbox = [bbox[0] - pad, bbox[1] - pad, bbox[2] + pad, bbox[3] + pad]

    print("[2/5] gathering events + weather...")
    events = tools.active_events(bbox=ev_bbox, days=30)
    weather = tools.weather_summary(g["lat"], g["lon"], past_days=7)

    print("[3/5] searching fresh imagery...")
    since = (today - dt.timedelta(days=days)).isoformat()
    imagery = tools.search_imagery(
        "earth-search",
        ["sentinel-2-l2a"],
        bbox=bbox,
        datetime_range=f"{since}T00:00:00Z/{today.isoformat()}T23:59:59Z",
        max_cloud_cover=40,
        limit=8,
    )

    best = min(
        imagery.get("items", []),
        key=lambda i: (i.get("cloud_cover") or 100),
        default=None,
    )

    change = None
    if best:
        print("[3.5/5] computing NDVI change vs baseline...")
        try:
            change = ndvi_change(best, today, days)
        except Exception as e:
            print(f"change detection skipped: {e}", file=sys.stderr)

    print("[4/5] rendering map + preview...")
    fc = {"type": "FeatureCollection", "features": []}
    for e in events.get("eonet", []):
        if e.get("coordinates"):
            fc["features"].append(
                {
                    "type": "Feature",
                    "geometry": {"type": "Point", "coordinates": e["coordinates"]},
                    "properties": {"title": e["title"], "description": f"{e['category']} · {e.get('date', '')[:10]}"},
                }
            )
    for e in events.get("gdacs", []):
        if e.get("coordinates") and e["coordinates"][0] is not None:
            fc["features"].append(
                {
                    "type": "Feature",
                    "geometry": {"type": "Point", "coordinates": e["coordinates"]},
                    "properties": {"title": e["title"], "description": f"GDACS {e['type']} · alert {e['alert_level']}"},
                }
            )
    layers = []
    preview_url = ""
    scene_caption = "No sufficiently cloud-free scene in the window."
    if best:
        layers.append(
            {
                "type": "item",
                "name": f"Sentinel-2 {best['datetime'][:10]} ({round(best.get('cloud_cover') or 0, 1)}% cloud)",
                "catalog": "earth-search",
                "collection_id": best["collection"],
                "item_id": best["id"],
                "assets": ["visual"],
            }
        )
        preview_url = tools.preview_item("earth-search", best["collection"], best["id"], max_size=768)["preview_url"]
        scene_caption = (
            f"Sentinel-2, {best['datetime'][:10]}, {round(best.get('cloud_cover') or 0, 1)}% cloud cover — "
            f"clearest of {imagery['count']} scenes in the last {days} days."
        )
    if fc["features"]:
        layers.append({"type": "geojson", "name": f"Active events ({len(fc['features'])})", "data": fc, "color": "#d63b3b"})

    out_dir.mkdir(parents=True, exist_ok=True)
    slug = "".join(ch if ch.isalnum() else "-" for ch in place.lower())[:40].strip("-")
    map_path = out_dir / f"brief-map-{slug}-{today.isoformat()}.html"
    tools.render_map(
        title=f"Earth brief: {place}",
        subtitle=f"{today.isoformat()} · latest imagery + active events",
        bbox=ev_bbox,
        layers=layers or [{"type": "geojson", "name": "AOI", "data": fc, "color": "#d63b3b"}],
        out_path=str(map_path),
    )

    print("[5/5] synthesizing brief with Claude...")
    data = {
        "place": place,
        "resolved_name": g["name"],
        "date": today.isoformat(),
        "aoi_bbox": bbox,
        "events": events,
        "weather": weather,
        "imagery": {"count": imagery.get("count"), "items": imagery.get("items", [])[:8]},
    }
    if change:
        data["ndvi_change"] = change
    state_path = Path(__file__).parent / "state" / f"{slug}.json"
    if state_path.exists():
        try:
            data["previous_run"] = json.loads(state_path.read_text(encoding="utf-8"))
        except Exception:
            pass
    md = synthesize(data)

    html = (
        PAGE.replace("__PLACE__", place)
        .replace("__DATE__", today.strftime("%A, %B %d, %Y"))
        .replace("__BODY__", md_to_html(md))
        .replace("__PREVIEW__", preview_url)
        .replace("__SCENE_CAPTION__", scene_caption)
        .replace("__MAP__", map_path.name)
    )
    brief_path = out_dir / f"brief-{slug}-{today.isoformat()}.html"
    brief_path.write_text(html, encoding="utf-8")
    (out_dir / f"brief-{slug}-{today.isoformat()}.md").write_text(md, encoding="utf-8")
    print(f"brief: {brief_path}\nmap:   {map_path}")
    alert_m = re.search(r"\b(CALM|WATCH|ACT)\b", md)
    tldr_m = re.search(r"## TL;DR\s*\n(.+?)(?=\n## |\Z)", md, re.S)
    result = {
        "place": place,
        "alert": alert_m.group(1) if alert_m else "CALM",
        "tldr": (tldr_m.group(1).strip() if tldr_m else "")[:400],
        "brief": brief_path.name,
        "map": map_path.name,
    }
    # memory for the next run: tomorrow's "what changed" diffs against this
    state_path.parent.mkdir(parents=True, exist_ok=True)
    state_path.write_text(json.dumps({
        "date": today.isoformat(),
        "alert": result["alert"],
        "event_titles": [e.get("title") for e in events.get("eonet", []) + events.get("gdacs", [])][:20],
        "ndvi_mean": change["current"]["mean"] if change else None,
        "scene_count": imagery.get("count"),
        "tldr": result["tldr"][:200],
    }, indent=1), encoding="utf-8")
    # also save the gathered data snapshot for the briefing evals
    (out_dir / f"brief-{slug}-{today.isoformat()}.data.json").write_text(
        json.dumps(data, default=str, indent=1), encoding="utf-8")
    return result


