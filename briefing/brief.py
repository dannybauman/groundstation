"""Earth briefs you: proactive AOI briefing built on groundstation tools.

Instead of you asking Earth the right question, Earth tells you what you need
to know. Gathers events (EONET/GDACS), weather (Open-Meteo), and fresh
imagery (STAC) for a place, has Claude synthesize a decision-ready brief, and
writes a shareable HTML page with an interactive map. Cron it for real dailies.

    uv run briefing/brief.py --place "Chelan County, Washington" --days 7
"""

