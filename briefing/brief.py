SYNTH_PROMPT = """You are writing a daily Earth briefing for a decision maker who cares about this
area of interest. You are given raw monitoring data as JSON. Write a concise brief in markdown with
exactly these sections:

## TL;DR
2-3 sentences: overall situation, the one thing that needs attention, alert level (CALM / WATCH / ACT).

## What changed
Bullets on events (fires, floods, storms) in or near the AOI — name, how close, trend if inferable.
If none, say so in one line.

## Weather signal
2-3 bullets: notable recent weather and the week ahead — heat, wind, precipitation, anything that
compounds the events above. Include numbers with units.

## Fresh eyes on the ground
Which new satellite scenes arrived, their dates and cloud cover, and what to look at first.

## Suggested next steps
2-4 concrete actions (e.g. "task a closer look at X", "compare NDVI vs last month", "no action needed").

Rules: be specific, cite dates, no filler, no speculation beyond the data. If data sections have
errors or are empty, work with what's there and note the gap in one clause, not a paragraph.
If an "ndvi_change" block is present, weave the vegetation delta (with both dates) into TL;DR and
What changed — a real change signal outranks scene bookkeeping.
If a "previous_run" block is present, "What changed" means changed SINCE THAT RUN: name events that
appeared, escalated, or cleared since it, and say plainly when today is calmer than the last run
(e.g. "yesterday's WATCH stands down"). Deltas against the last run outrank everything else.

DATA:
"""

